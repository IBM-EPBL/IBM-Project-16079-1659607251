

list=[20,10,23,30,40]
print(list)

#insertion
list.insert(2,20)
print(list)

#deletion of first occurence of integer
del list[0]
print(list)

#insertion at end of list
list.append(-2)
print(list)

#sorting
list.sort()
print(list)

#popping last element
list.pop()
print(list)

#reverse the list
list.reverse()
print(list)

